const BURL="https://v6.exchangerate-api.com/v6/ADD_YOUR_KEY/latest";
const dd=document.querySelectorAll(".dropdown select")
const convbutt=document.querySelector("form button")
const frm=document.querySelector(".from select")
const to=document.querySelector(".to select")
const showmsg=document.querySelector(".message")

for(let s of dd){
    for(let c in countryList){
        let newop=document.createElement("option")
        newop.innerText=c
        if(s.name==="from" && c==="USD"){
            newop.selected="selected"
        } else if(s.name==="to" && c==="INR"){
            newop.selected="selected"
        }
        s.append(newop)
    }
    s.addEventListener("change",(evt)=>{
        updateflag(evt.target)
    })
}
const updateflag=(element)=>{
    let currc=element.value
    let countc=countryList[currc]
    let newflagl=`https://flagsapi.com/${countc}/flat/64.png`
    let img=element.parentElement.querySelector("img")
    img.src=newflagl
}
convbutt.addEventListener("click",async (evt)=>{
    evt.preventDefault() //Avoids the refresh of the page upon clicking on the button.
    let amt=document.querySelector(".amount input")
    let amtval=amt.value
    if(amtval===""||amtval<1){
        amtval=1
        amt.value=amtval
    }
    const URL=`${BURL}/${frm.value}`
    let response=await fetch(URL)
    let data= await response.json()
    let dataspc=data["conversion_rates"]
    let dataval=dataspc[to.value]
    let ans=amtval*dataval
    showmsg.innerText=`${amt.value}${frm.value} = ${ans}${to.value}`
})